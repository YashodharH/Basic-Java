package com;

public class Bitwise {

	public static void main(String[] args) {
		int a =20;
		int b =24;
		int c = (a+b);
		System.out.println(c);
		System.out.println(36&23);
		System.out.println(36|23);
		System.out.println(36^23);
		System.out.println(25&27);
		System.out.println(25|27);
		System.out.println(19&7);
		System.out.println(19|7);
		
	}

}
