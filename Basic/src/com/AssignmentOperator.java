package com;

public class AssignmentOperator {

	public static void main(String[] args) {

		double a= 2,b=8;

		System.out.println(a+=b);
		System.out.println(a-=b);
		System.out.println(a/=b);
		System.out.println(a*=b);
		System.out.println(a%=b);
		System.out.println(a);
	}

}
